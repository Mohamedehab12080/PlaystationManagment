
package Model;

public class devicesModel {

    private int id;
    private String deviceName;
    private float priceofSingle;
    private float priceofMulti;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public float getPriceofSingle() {
        return priceofSingle;
    }

    public void setPriceofSingle(float priceofSingle) {
        this.priceofSingle = priceofSingle;
    }

    public float getPriceofMulti() {
        return priceofMulti;
    }

    public void setPriceofMulti(float priceofMulti) {
        this.priceofMulti = priceofMulti;
    }
    
}
